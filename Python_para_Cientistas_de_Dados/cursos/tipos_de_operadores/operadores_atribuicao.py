# atribuição simples
saldo = 500
print(saldo)

# atribuição adição
saldo = 500
saldo += 200
print(saldo)

# atribuição subtração
saldo = 500
saldo -= 100
print(saldo)

# atribuição multiplicação
saldo = 500
saldo *= 2
print(saldo)

# atribuição divisão
saldo = 500
saldo /= 5
print(saldo)

# atribuição divisão inteira
saldo = 500
saldo //= 5
print(saldo)

# atribuição módulo
saldo = 500
saldo %= 480
print(saldo)

# atribuição exponenciação
saldo = 80
saldo **= 2
print(saldo)