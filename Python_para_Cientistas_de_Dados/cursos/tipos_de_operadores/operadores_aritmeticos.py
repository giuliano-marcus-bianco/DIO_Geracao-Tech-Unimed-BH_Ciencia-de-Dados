produto_1 = 20
produto_2 = 10

# adição
print(produto_1 + produto_2)

#subtração
print(produto_1 - produto_2)

# divisão
print(produto_1 / produto_2)

# divisão inteira
print(produto_1 // produto_2)

# multiplicação
print(produto_1 * produto_2)

# módulo
print(produto_1 % produto_2)

# exponenciação
print(produto_1 ** produto_2)

# ordem de precedência das operações:
# 1. parênteses;
# 2. exponenciação;
# 3. divisão e multiplicação (da esquerda para a direita);
# 4. soma e subtração (da esquerda para a direita).

# boa prática: para melhorar a leitura e compreesão do código, quando montar variáveis grandes, deixar explicíto com parênteses a ordem de precedência das operações, mesmo que não seja necessaário o uso dos parênteses.